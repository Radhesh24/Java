

public class Type_Conversion  {
    
    public static void main(String args[]){

        // Type Conversion in Java is possible only when 
        // 1. Data is compatible 
        // 2. No Data loss

        // Also called as Widening and Implicit Conversion
        // Implicit Conversion is automatically carried out by Java

        byte a = 10;
        short b = a;

        System.out.println(b);


    }
}
