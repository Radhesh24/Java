import java.util.*;

public class while_loop {
    public static void main(String args[]){

        // While loop executes itself until a certain condition is true

        Scanner sc = new Scanner(System.in);

        int i = 1;
        while (i <= 10){
            System.out.println(i);
            i++;
        }
        
        int n = sc.nextInt();
        int a = 1;
        while (a <= n){
            System.out.println(a);
            a++;
        }

        
        

        }
    }

